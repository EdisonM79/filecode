package com.jzsk.filecode.model.form;

public class ThreeForm {

	private String createUser;

	private String department;

	private String twoId;

	private String threeVersion;

	private String threeName;

	private String year;

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getCreateUser() {
		return createUser;
	}

	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getTwoId() {
		return twoId;
	}

	public void setTwoId(String twoId) {
		this.twoId = twoId;
	}

	public String getThreeVersion() {
		return threeVersion;
	}

	public void setThreeVersion(String threeVersion) {
		this.threeVersion = threeVersion;
	}

	public String getThreeName() {
		return threeName;
	}

	public void setThreeName(String threeName) {
		this.threeName = threeName;
	}

}
